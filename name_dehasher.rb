$localize = {}
def init_localize
    json_filename = "localize.json"
    return false unless File.exist? json_filename

    text = File.read json_filename

    # custom parser bc i was feeling quirky
    text.scan(/\"([A-F\d]+)\":\s*\"(.*?)(?<!\\)\"/).each do |hash, tr|
        hash = hash.to_i 16
        $localize[hash] = tr unless $localize.has_key? hash
    end

    return true
end

def fix_func_names(text)
    text.gsub(/(\S+?)\[0x([A-F\d]+)\]/){
        dehashed = $glob_names[$2.to_i(16)];
        !dehashed.nil? ? "#$1\[ \"#{dehashed}\" --\[\[DH\]\] \]" : "#$1\[ 0x#{$2} \]";
    }
end

def fix_str_names(text)
    text.gsub(/0x([A-F\d]+)/){
        dehashed = $localize[$1.to_i(16)];
        !dehashed.nil? ? "\"#{dehashed}\" --\[\[DH\]\]" : $&;
    }   
end

